#include "3-calc.h"
/**
 * op_add - calc the sum of two int
 * @a:frist int
 * @b: second int
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - calc the differecne
 * @a: 1 int
 * @b: 2 int
 *
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - calc the product
 * @a: 1 int
 * @b: 2 int
 *
 * Return: the difference
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - calc the division
 * @a: 1 int
 * @b: 2 int
 *
 * Return: the division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calc the remainder of th div
 * @a: 1 int
 * @b: 2 int
 *
 * Return: the remainder of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
